async def seed_database() -> int:
    """Создать таблицы и заполнить тестовыми данными. Возвращает кол-во книг."""
    async with engine.begin() as conn:
        await conn.run_sync(Base.metadata.create_all)

    async with async_session() as session:
        # Уже есть данные?
        count = (await session.execute(select(func.count()).select_from(Book))).scalar() or 0
        if count > 0:
            return count

        # Создаём авторов
        author_map: dict[str, Author] = {}
        for name, slug, bio in AUTHORS:
            author = Author(name=name, slug=slug, bio=bio)
            session.add(author)
            author_map[slug] = author

        await session.flush()  # чтобы получить id авторов

        # Создаём книги
        total = 0
        for slug, books_data in BOOKS_BY_AUTHOR.items():
            author = author_map[slug]
            for title, desc, isbn, year, rating in books_data:
                book = Book(
                    title=title,
                    description=desc,
                    isbn=isbn,
                    author_id=author.id,
                    year=year,
                    rating=rating,
                    is_available=True,
                    views_count=random.randint(0, 10000),
                )
                session.add(book)
                total += 1

        await session.commit()
        return total
